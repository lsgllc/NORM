/**
 * This is the AbstractResponse class, for "shared" webservice
 * responses
 *
 * @version	Special Plates	03/02/2007
 * @author	Bob Brown
 * <br>Creation Date:		03/02/2007 14:30:00
 */
public abstract class AbstractResponse
{
	//	application-level acknowledgement
	private String ack = "";
	private String build = "";
	private Errors errors = null;
	private Date timestamp = null;
	private String version = "";

	/**
	 * A token representing the application-level 
	 * acknowledgement code.
	 * See the ACK Constants.
	 * 
	 * @return
	 */
	public String getAck()
	{
		return ack;
	}

	/**
	 * A string representing the specific software 
	 * build that processes the request and generates 
	 * the response.  This is the date.
	 * 
	 * @return String
	 */
	public String getBuild()
	{
		return build;
	}

	/**
	 * Error code and messages for debugging a response.
	 * See the Errors class.
	 * 
	 * @return Errors
	 */
	public Errors getErrors()
	{
		return errors;
	}

	/**
	 * The date and time the response was generated by the Web service 
	 * as a result of processing a request.
	 * 
	 * @return Date
	 */
	public Date getTimestamp()
	{
		return timestamp;
	}

	/**
	 * A string representing the specific software 
	 * version that processes the request and generates 
	 * the response.
	 * 
	 * @return String
	 */
	public String getVersion()
	{
		return version;
	}

	/**
	 * Sets the token representing the application-level 
	 * acknowledgement code.
	 * 
	 * @param asAck String
	 */
	public void setAck(String asAck)
	{
		ack = asAck;
	}

	/**
	 * Sets the string representing the specific software 
	 * build that processes the request and generates 
	 * the response.  This is the date.
	 * 
	 * @param asBuild String
	 */
	public void setBuild(String asBuild)
	{
		build = asBuild;
	}

	/**
	 * Sets the Error code and messages for debugging a 
	 * response.
	 * See the Errors class.
	 * 
	 * @param aaErrors Errors
	 */
	public void setErrors(Errors aaErrors)
	{
		errors = aaErrors;
	}

	/**
	 * Sets the date and time the response was generated 
	 * by the Web Service as a result of processing a request.
	 * 
	 * @param aaTimestamp Date
	 */
	public void setTimestamp(Date aaTimestamp)
	{
		timestamp = aaTimestamp;
	}

	/**
	 * Sets the string representing the specific software 
	 * version that processes the request and generates 
	 * the response.
	 * 
	 * @param asVersion String
	 */
	public void setVersion(String asVersion)
	{
		version = asVersion;
	}

}